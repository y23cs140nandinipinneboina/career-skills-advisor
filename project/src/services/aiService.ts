const calculateMatchScore = (
  userSkills: any[],
  interests: string[],
  requiredSkillsForCareer: string[]
): number => {
  const userSkillNames = userSkills.map(s => s.name.toLowerCase());
  const matchingSkills = requiredSkillsForCareer.filter(skill => 
    userSkillNames.some(userSkill => userSkill.includes(skill.toLowerCase()))
  );
  
  const skillMatch = (matchingSkills.length / requiredSkillsForCareer.length) * 60;
  const interestBonus = interests.length > 0 ? Math.min(interests.length * 5, 25) : 0;
  const randomVariation = Math.random() * 15; // Add some variation
  
  return Math.min(Math.round(skillMatch + interestBonus + randomVariation), 95);
};

const getMissingSkills = (userSkills: any[], requiredSkills: string[]): string[] => {
  const userSkillNames = userSkills.map(s => s.name.toLowerCase());
  return requiredSkills.filter(skill => 
    !userSkillNames.some(userSkill => userSkill.includes(skill.toLowerCase()))
  );
};

export const analyzeSkillsGap = (
  userSkills: any[],
  targetCareer: CareerRecommendation
): { gaps: string[]; recommendations: string[] } => {
  const userSkillNames = userSkills.map(s => s.name.toLowerCase());
  const gaps = targetCareer.requiredSkills.filter(skill => 
    !userSkillNames.some(userSkill => userSkill.includes(skill.toLowerCase()))
  );
  
  const recommendations = gaps.map(skill => 
    `Consider learning ${skill} to improve your fit for this role`
  );
  
  return { gaps, recommendations };
};